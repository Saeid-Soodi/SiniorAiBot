function helpKeyboard() {
  const channelUrl = normalizeTelegramUrl(env.channelUrl, env.channelUsername);
  const rows = [
    [{ text: '🎓 آموزش‌های AI', callback_data: 'help_lessons' }, { text: '👤 حساب من', callback_data: 'my_account' }],
    [{ text: '🛟 پشتیبانی', callback_data: 'help_support' }]
  ];
  if (channelUrl) rows.push([{ text: '📢 کانال Sinior Ai', url: channelUrl }]);
  return { inline_keyboard: rows };
}

async function sendHelp(ctx) {
  const sections = helpSections();
  const fullText = sections.join('\n\n━━━━━━━━━━━━━━\n\n');
  return sendLongMessage(ctx, fullText, {
    parse_mode: 'HTML',
    disable_web_page_preview: true,
    reply_markup: helpKeyboard()
  }, { maxLength: 3600 });
}

module.exports = { sendHelp, helpSections, helpKeyboard };
