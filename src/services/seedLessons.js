async function seedLessons(adminId) {
  if (await AiLesson.countDocuments() > 0) return;
  await AiLesson.insertMany(lessons.map(([title, content], index) => ({
    title,
    content,
    category: 'prompt-writing',
    accessLevel: 'free',
    isActive: true,
    createdBy: adminId,
    order: index + 1
  })));
  console.log('✅ 10 starter AI lessons seeded');
}

module.exports = seedLessons;
